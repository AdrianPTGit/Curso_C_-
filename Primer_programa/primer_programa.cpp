#include <iostream>  
// Incluye la librería estándar de entrada y salida.
// Permite usar cout, cin, endl, etc.

using namespace std;  
// Evita tener que escribir std::cout, std::endl, std::cin, etc.

int main()  
// Función principal del programa.
// Es el punto de entrada: aquí empieza la ejecución.
{
    cout << "Hola Mundo" << endl;  
    // Muestra el texto "Hola Mundo" por pantalla
    // endl inserta un salto de línea y vacía el buffer de salida

    return 0;  
    // Indica que el programa terminó correctamente
}


/*  | Elemento               | Función                 |
    | ---------------------- | ----------------------- |
    | `#include <iostream>`  | Entrada/salida estándar |
    | `using namespace std;` | Acceso directo a la STL |
    | `main()`               | Punto de inicio         |
    | `cout`                 | Salida por consola      |
    | `endl`                 | Salto de línea          |
    | `return 0;`            | Fin correcto            |*/
