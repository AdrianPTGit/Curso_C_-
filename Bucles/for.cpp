#include <iostream>   // Librería para entrada y salida estándar

using namespace std;  // Permite usar cout sin escribir std::

int main()
{
    // Bucle for clásico
    // 1. Inicialización: int i = 1
    // 2. Condición: mientras i <= 5
    // 3. Incremento: i aumenta en 1 cada vuelta
    for (int i = 1; i <= 5; i++)
    {
        cout << "Valor de i: " << i << endl;  // Muestra el valor de i
    }

    return 0;  // Finaliza el programa correctamente
}
